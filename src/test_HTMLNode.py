class TestHTMLNode(unittest.TestCase):
    def test_eq(self):
        # Check if 'other' is also an HTMLNode
        node = HTMLNode("a", "This is a text node", None, None)
        node2 = HTMLNode("a", "This is a text node", None, None)
        self.assertEqual(node.tag, node2.tag)
        self.assertEqual(node.value, node2.value)
        self.assertEqual(node.children, node2.children)
        self.assertEqual(node.props, node2.props)
        

        

    def test_eq_false(self):
        # Test that different text_types make nodes unequal
        node = HTMLNode("a", "This is a text node", None, None)
        node2 = HTMLNode("a", "This is a different text node", None, None)
        assert node != node2

    def test_leaf_to_html_p(self):
        node = LeafNode("p", "Hello, world!")
        self.assertEqual(node.to_html(), "<p>Hello, world!</p>")
    def test_to_html_with_children(self):
        child_node = LeafNode("span", "child")
        parent_node = ParentNode("div", [child_node])
        self.assertEqual(parent_node.to_html(), "<div><span>child</span></div>")

    def test_to_html_with_grandchildren(self):
        grandchild_node = LeafNode("b", "grandchild")
        child_node = ParentNode("span", [grandchild_node])
        parent_node = ParentNode("div", [child_node])
        self.assertEqual(
            parent_node.to_html(),
            "<div><span><b>grandchild</b></span></div>",
        )
